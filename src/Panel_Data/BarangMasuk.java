        public void cariData(){
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.getDataVector().removeAllElements();
        tabel.addColumn("ID Barang");
        tabel.addColumn("Nama Barang");
        tabel.addColumn("Harga Barang");
        
        try {
            Connection c = ConnectionDb.Connect();
            String sql = "Select * from barang_masuk where id_barang like '%" + cari.getText() + "%'" +
                    "or nama_barang like '%" + cari.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {                
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                });
            }
            table.setModel(tabel);
            System.out.println("Data Berhasil Ditemukan");
        } catch (Exception e) {
            System.out.println("Cari Data Error" +e);
        }finally{
        }
    }
