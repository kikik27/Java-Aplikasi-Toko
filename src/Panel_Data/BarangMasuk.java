    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String tanggal = Tanggal.getText();
        String id = ID.getText();
        String nama = Nama.getText();
        String stok = Stok.getText();

        if (ID.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Isi ID Barang","Eror",JOptionPane.ERROR_MESSAGE);
        }else if(Nama.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Isi Nama Barang","Eror",JOptionPane.ERROR_MESSAGE);
        }else if(Stok.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Isi Stok Barang","Eror",JOptionPane.ERROR_MESSAGE);
        }else{
            try{Connection c = ConnectionDb.Connect();
                String sql = "INSERT INTO barang_masuk VALUES (?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, tanggal);
                p.setString(2, id);
                p.setString(3, nama);
                p.setString(4, stok);
                JOptionPane.showMessageDialog(null,"Data Tersimpan !");
                System.out.println("Data Tersimpan");
                p.executeUpdate();
                p.close();
                kosongkan();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,"Simpan Data Gagal"+ex);}
        }
        showTable();
    }//GEN-LAST:event_SimpanActionPerformed

